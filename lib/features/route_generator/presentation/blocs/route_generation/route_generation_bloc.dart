/// BLoC pour g√©rer l'analyse de zone et la g√©n√©ration de parcours
/// üÜï Int√©gr√© avec l'architecture UI First pour les cr√©dits
class RouteGenerationBloc extends HydratedBloc<RouteGenerationEvent, RouteGenerationState> {
  final RoutesRepository _routesRepository;
  final CreditsBloc _creditsBloc;
  final CreditsRepository _creditsRepository;
  final AppDataBloc? _appDataBloc; // üÜï R√©f√©rence √† AppDataBloc

  RouteGenerationBloc({
    RoutesRepository? routesRepository,
    required CreditsBloc creditsBloc,
    CreditsRepository? creditsRepository,
    AppDataBloc? appDataBloc, // üÜï Param√®tre optionnel
  }) : _routesRepository = routesRepository ?? RoutesRepository(),
       _creditsBloc = creditsBloc,
       _creditsRepository = creditsRepository ?? CreditsRepository(),
       _appDataBloc = appDataBloc, // üÜï Injection
       super(const RouteGenerationState()) {
    
    on<ZoneAnalysisRequested>(_onZoneAnalysisRequested);
    on<RouteGenerationRequested>(_onRouteGenerationRequested);
    on<GeneratedRouteSaved>(_onGeneratedRouteSaved);
    on<SavedRouteLoaded>(_onSavedRouteLoaded);
    on<ZoneAnalysisCleared>(_onZoneAnalysisCleared);
    on<SavedRouteDeleted>(_onSavedRouteDeleted);
    on<SavedRoutesRequested>(_onSavedRoutesRequested);
    on<RouteUsageUpdated>(_onRouteUsageUpdated);
    on<SyncPendingRoutesRequested>(_onSyncPendingRoutesRequested);
    on<RouteStateReset>(_onRouteStateReset);
  }

  /// Analyse de zone simplifi√©e
  Future<void> _onZoneAnalysisRequested(
    ZoneAnalysisRequested event,
    Emitter<RouteGenerationState> emit,
  ) async {
    emit(state.copyWith(
      isAnalyzingZone: true,
      errorMessage: null,
    ));

    try {
      await Future.delayed(Duration(milliseconds: 500));

      final stats = ZoneStatistics(
        parksCount: 0,
        waterPointsCount: 0,
        viewPointsCount: 0,
        drinkingWaterCount: 0,
        toiletsCount: 0,
        greenSpaceRatio: 0.3,
        suitabilityLevel: 'good',
      );

      emit(state.copyWith(
        isAnalyzingZone: false,
        pois: [_createDummyPoi(event.latitude, event.longitude)],
        zoneStats: stats,
        errorMessage: null,
      ));

    } catch (e) {
      emit(state.copyWith(
        isAnalyzingZone: false,
        errorMessage: 'Erreur lors de l\'analyse de la zone: $e',
      ));
    }
  }

  /// üÜï G√©n√©ration avec architecture UI First pour les cr√©dits
  Future<void> _onRouteGenerationRequested(
    RouteGenerationRequested event,
    Emitter<RouteGenerationState> emit,
  ) async {
    const int REQUIRED_CREDITS = 1; // Co√ªt d'une g√©n√©ration
    final generationId = DateTime.now().millisecondsSinceEpoch.toString();
    
    try {
      print('üöÄ === D√âBUT G√âN√âRATION UI FIRST (ID: $generationId) ===');
      print('üèÅ Bypass credit check: ${event.bypassCreditCheck}');

      emit(state.copyWith(
        isGeneratingRoute: true,
        errorMessage: null,
        stateId: '$generationId-start',
      ));

      // ===== üÜï NOUVELLE LOGIQUE CONDITIONNELLE =====
      bool hasEnoughCredits = true; // Par d√©faut OK pour les guests
      int currentCredits = 0;
      
      // üîß CORRECTION PRINCIPALE : V√©rifier les cr√©dits SEULEMENT si pas de bypass
      if (!event.bypassCreditCheck) {
        print('üí≥ === V√âRIFICATION CR√âDITS POUR UTILISATEUR AUTHENTIFI√â ===');
        
        // V√©rification double de l'authentification
        final currentUser = su.Supabase.instance.client.auth.currentUser;
        if (currentUser == null) {
          print('‚ùå Session Supabase expir√©e pendant la g√©n√©ration');
          emit(state.copyWith(
            isGeneratingRoute: false,
            errorMessage: 'Session expir√©e. Veuillez vous reconnecter.',
            stateId: '$generationId-session-expired',
          ));
          return;
        }
        
        // V√©rification cr√©dits via AppDataBloc ou API
        if (_appDataBloc != null && _appDataBloc.state.isCreditDataLoaded) {
          currentCredits = _appDataBloc.state.availableCredits;
          hasEnoughCredits = _appDataBloc.state.hasCredits && currentCredits >= REQUIRED_CREDITS;
          
          print('üí∞ V√©rification cr√©dits (UI First): $currentCredits disponibles, ${hasEnoughCredits ? "‚úÖ" : "‚ùå"} suffisant');
        } else {
          // Fallback: v√©rification traditionnelle (plus lente)
          print('‚ö†Ô∏è Donn√©es cr√©dits non disponibles, v√©rification via API...');
          try {
            hasEnoughCredits = await _creditsBloc.hasEnoughCredits(REQUIRED_CREDITS);
            currentCredits = await getAvailableCredits();
            print('üí∞ V√©rification cr√©dits (API): $REQUIRED_CREDITS requis, $currentCredits disponibles ‚Üí ${hasEnoughCredits ? "‚úÖ" : "‚ùå"}');
          } catch (e) {
            print('‚ùå Erreur API cr√©dits: $e');
            emit(state.copyWith(
              isGeneratingRoute: false,
              errorMessage: 'Impossible de v√©rifier les cr√©dits. Veuillez r√©essayer.',
              stateId: '$generationId-credit-check-error',
            ));
            return;
          }
        }

        // Gestion de l'insuffisance de cr√©dits
        if (!hasEnoughCredits) {
          emit(state.copyWith(
            isGeneratingRoute: false,
            errorMessage: 'Cr√©dits insuffisants pour g√©n√©rer un parcours. Vous avez $currentCredits cr√©dits, mais il en faut $REQUIRED_CREDITS.',
            stateId: '$generationId-insufficient-credits',
          ));
          return;
        }

        print('‚úÖ Cr√©dits suffisants, lancement de la g√©n√©ration');

        // Mise √† jour optimiste du solde dans AppDataBloc
        final newBalance = currentCredits - REQUIRED_CREDITS;
        if (_appDataBloc != null) {
          _appDataBloc.add(CreditBalanceUpdatedInAppData(
            newBalance: newBalance,
            isOptimistic: true,
          ));
          print('‚ö° Mise √† jour optimiste: $currentCredits ‚Üí $newBalance cr√©dits');
        }
      } else {
        print('üÜï === MODE GUEST - BYPASS TOTAL V√âRIFICATION CR√âDITS ===');
        print('üÜï G√©n√©ration guest autoris√©e, aucune v√©rification de cr√©dits');
      }
      
      // ===== G√âN√âRATION DU PARCOURS =====
      
      print('üõ£Ô∏è G√©n√©ration du parcours via API...');
      final result = await GraphHopperApiService.generateRoute(parameters: event.parameters);

      print('‚úÖ G√©n√©ration r√©ussie: ${result.coordinates.length} points, ${result.distanceKm}km');

      // ===== UTILISATION DES CR√âDITS AVEC SYNCHRONISATION (SEULEMENT POUR AUTH) =====
      
      if (!event.bypassCreditCheck) {
        // Consommer les cr√©dits pour les utilisateurs authentifi√©s
        print('üí≥ Utilisation de $REQUIRED_CREDITS cr√©dit(s)...');
        final usageResult = await _creditsRepository.useCredits(
          amount: REQUIRED_CREDITS,
          reason: 'Route generation',
          routeGenerationId: generationId,
          metadata: {
            'activity_type': event.parameters.activityType.name,
            'distance_km': event.parameters.distanceKm,
            'terrain_type': event.parameters.terrainType.name,
            'urban_density': event.parameters.urbanDensity.name,
          },
        );

        if (usageResult.success && usageResult.updatedCredits != null) {
          print('‚úÖ Utilisation de cr√©dits r√©ussie');
          print('üí∞ Nouveau solde: ${usageResult.updatedCredits!.availableCredits}');
        } else {
          print('‚ùå √âchec utilisation cr√©dits: ${usageResult.errorMessage}');
          
          // Annuler la mise √† jour optimiste
          if (_appDataBloc != null) {
            _appDataBloc.add(CreditBalanceUpdatedInAppData(
              newBalance: currentCredits, // Restaurer la valeur originale
              isOptimistic: false,
            ));
          }

          emit(state.copyWith(
            isGeneratingRoute: false,
            errorMessage: 'Erreur lors de l\'utilisation des cr√©dits: ${usageResult.errorMessage}',
            stateId: '$generationId-credit-error',
          ));
          return;
        }
      } else {
        print('üÜï === G√âN√âRATION GUEST - PAS D\'UTILISATION DE CR√âDITS ===');
      }

      // Mettre √† jour l'√©tat avec le parcours g√©n√©r√©
      emit(state.copyWith(
        generatedRoute: result.coordinates,
        isGeneratingRoute: false,
        usedParameters: event.parameters,
        routeMetadata: result.metadata,
        errorMessage: null,
        stateId: '$generationId-success',
      ));

      if (!event.bypassCreditCheck) {
        print('‚úÖ === FIN G√âN√âRATION UI FIRST (SUCCESS: $generationId) ===');
        print('üí≥ $REQUIRED_CREDITS cr√©dit(s) utilis√©(s)');
      } else {
        print('‚úÖ === FIN G√âN√âRATION GUEST (SUCCESS: $generationId) ===');
        print('üÜì G√©n√©ration gratuite utilis√©e');
      }

    } catch (e) {
      print('‚ùå Erreur g√©n√©ration: $e');
      
      // Annuler la mise √† jour optimiste en cas d'erreur (seulement si auth)
      if (!event.bypassCreditCheck && _appDataBloc != null) {
        try {
          final realCredits = await _creditsRepository.getUserCredits();
          _appDataBloc.add(CreditBalanceUpdatedInAppData(
            newBalance: realCredits.availableCredits,
            isOptimistic: false,
          ));
          print('üîß Solde des cr√©dits restaur√©: ${realCredits.availableCredits}');
        } catch (creditError) {
          print('‚ö†Ô∏è Impossible de restaurer le solde des cr√©dits: $creditError');
        }
      }

      emit(state.copyWith(
        isGeneratingRoute: false,
        errorMessage: 'Erreur lors de la g√©n√©ration du parcours: $e',
        stateId: '$generationId-exception',
      ));
    }
  }

  /// üÜï Sauvegarde du parcours via RoutesRepository
  Future<void> _onGeneratedRouteSaved(
    GeneratedRouteSaved event,
    Emitter<RouteGenerationState> emit,
  ) async {
    if (!state.hasGeneratedRoute || state.usedParameters == null) {
      emit(state.copyWith(
        errorMessage: 'Aucun parcours √† sauvegarder',
      ));
      return;
    }

    emit(state.copyWith(
      isSavingRoute: true,
      errorMessage: null,
    ));

    try {
      print('üöÄ D√©but sauvegarde avec screenshot pour: ${event.name}');

      // 1. üì∏ Capturer le screenshot de la carte
      String? screenshotUrl;
      try {
        print('üì∏ Capture du screenshot...');
        screenshotUrl = await ScreenshotService.captureAndUploadMapSnapshot(
          liveMap: event.map,
          routeCoords: state.generatedRoute!,
          routeId: 'temp_${DateTime.now().millisecondsSinceEpoch}',
          userId: 'temp_user',
        );

        if (screenshotUrl != null) {
          print('‚úÖ Screenshot captur√© avec succ√®s: $screenshotUrl');
        } else {
          print('‚ö†Ô∏è Screenshot non captur√©, sauvegarde sans image');
        }
      } catch (screenshotError) {
        print('‚ùå Erreur capture screenshot: $screenshotError');
        screenshotUrl = null;
      }

      // 2. üíæ Sauvegarder le parcours avec l'URL de l'image
      final actualDistanceKm = state.routeMetadata?['distanceKm'] as double? ?? 
          _calculateRouteDistance(state.generatedRoute!);
      
      final savedRoute = await _routesRepository.saveRoute(
        name: event.name,
        parameters: state.usedParameters!,
        coordinates: state.generatedRoute!,
        actualDistance: actualDistanceKm,
        estimatedDuration: state.routeMetadata?['durationMinutes'] as int?,
        imageUrl: screenshotUrl,
      );

      // 3. üÜï Synchroniser avec AppDataBloc pour l'historique
      _appDataBloc?.add(SavedRouteAddedToAppData(
        name: event.name,
        parameters: state.usedParameters!,
        coordinates: state.generatedRoute!,
        actualDistance: actualDistanceKm,
        estimatedDuration: state.routeMetadata?['durationMinutes'] as int?,
        map: event.map,
      ));

      // 4. üîÑ Mettre √† jour la liste des parcours sauvegard√©s
      final updatedRoutes = List<SavedRoute>.from(state.savedRoutes)
        ..add(savedRoute);

      emit(state.copyWith(
        isSavingRoute: false,
        savedRoutes: updatedRoutes,
        errorMessage: null,
      ));

      print('‚úÖ Parcours sauvegard√© avec succ√®s: ${savedRoute.name} (${savedRoute.formattedDistance})');
      print('üñºÔ∏è Image: ${savedRoute.hasImage ? "‚úÖ Captur√©e" : "‚ùå Aucune"}');

    } catch (e) {
      print('‚ùå Erreur sauvegarde compl√®te: $e');
      emit(state.copyWith(
        isSavingRoute: false,
        errorMessage: 'Erreur lors de la sauvegarde: ${e.toString()}',
      ));
    }
  }

  /// üÜï Chargement des parcours sauvegard√©s
  Future<void> _onSavedRoutesRequested(
    SavedRoutesRequested event,
    Emitter<RouteGenerationState> emit,
  ) async {
    try {
      emit(state.copyWith(isAnalyzingZone: true));

      // üÜï Prioriser AppDataBloc si disponible
      List<SavedRoute> routes;
      if (_appDataBloc != null && _appDataBloc.state.hasHistoricData) {
        routes = _appDataBloc.state.savedRoutes;
        print('üì¶ Parcours charg√©s depuis AppDataBloc (${routes.length})');
      } else {
        routes = await _routesRepository.getUserRoutes();
        print('üåê Parcours charg√©s depuis API (${routes.length})');
      }

      emit(state.copyWith(
        isAnalyzingZone: false,
        savedRoutes: routes,
        errorMessage: null,
      ));

      print('‚úÖ ${routes.length} parcours charg√©s');

    } catch (e) {
      emit(state.copyWith(
        isAnalyzingZone: false,
        errorMessage: 'Erreur lors du chargement des parcours: $e',
      ));
    }
  }

  /// üÜï Suppression d'un parcours
  Future<void> _onSavedRouteDeleted(
    SavedRouteDeleted event,
    Emitter<RouteGenerationState> emit,
  ) async {
    try {
      await _routesRepository.deleteRoute(event.routeId);

      // üÜï Synchroniser avec AppDataBloc
      _appDataBloc?.add(SavedRouteDeletedFromAppData(event.routeId));

      final updatedRoutes = state.savedRoutes
          .where((r) => r.id != event.routeId)
          .toList();

      emit(state.copyWith(
        savedRoutes: updatedRoutes,
      ));

      print('‚úÖ Parcours supprim√©: ${event.routeId}');

    } catch (e) {
      emit(state.copyWith(
        errorMessage: 'Erreur lors de la suppression: $e',
      ));
    }
  }

  /// Chargement d'un parcours sauvegard√©
  Future<void> _onSavedRouteLoaded(
    SavedRouteLoaded event,
    Emitter<RouteGenerationState> emit,
  ) async {
    try {
      print('üîÑ Chargement du parcours sauvegard√©: ${event.routeId}');
      
      // üÜï Prioriser AppDataBloc si disponible
      SavedRoute? route;
      if (_appDataBloc != null && _appDataBloc.state.hasHistoricData) {
        route = _appDataBloc.state.savedRoutes.firstWhere(
          (r) => r.id == event.routeId,
          orElse: () => throw Exception('Parcours non trouv√©'),
        );
        print('üì¶ Parcours charg√© depuis AppDataBloc');
      } else {
        final routes = await _routesRepository.getUserRoutes();
        route = routes.firstWhere(
          (r) => r.id == event.routeId,
          orElse: () => throw Exception('Parcours non trouv√©'),
        );
        print('üåê Parcours charg√© depuis API');
      }

      // Calculer les m√©tadonn√©es
      final metadata = {
        'distanceKm': route.actualDistance ?? route.parameters.distanceKm,
        'distance': ((route.actualDistance ?? route.parameters.distanceKm) * 1000).round(),
        'durationMinutes': route.actualDuration ?? 0,
        'points_count': route.coordinates.length,
        'is_loop': route.parameters.isLoop,
      };

      // Mettre √† jour l'√©tat avec le parcours charg√©
      emit(state.copyWith(
        generatedRoute: route.coordinates,
        usedParameters: route.parameters,
        routeMetadata: metadata,
        isLoadedFromHistory: true,
        errorMessage: null,
        stateId: 'loaded-${event.routeId}',
      ));

      print('‚úÖ Parcours charg√© avec succ√®s: ${route.name}');

    } catch (e) {
      print('‚ùå Erreur chargement parcours: $e');
      emit(state.copyWith(
        errorMessage: 'Erreur lors du chargement du parcours: $e',
        stateId: 'error-${event.routeId}',
      ));
    }
  }

  /// üÜï Mise √† jour des statistiques d'utilisation
  Future<void> _onRouteUsageUpdated(
    RouteUsageUpdated event,
    Emitter<RouteGenerationState> emit,
  ) async {
    try {
      await _routesRepository.updateRouteUsage(event.routeId);
      
      // üÜï Synchroniser avec AppDataBloc
      _appDataBloc?.add(SavedRouteUsageUpdatedInAppData(event.routeId));
      
      // Mettre √† jour localement aussi
      final updatedRoutes = state.savedRoutes.map((route) {
        if (route.id == event.routeId) {
          return route.copyWith(
            timesUsed: route.timesUsed + 1,
            lastUsedAt: DateTime.now(),
          );
        }
        return route;
      }).toList();

      emit(state.copyWith(savedRoutes: updatedRoutes));

    } catch (e) {
      print('‚ùå Erreur mise √† jour statistiques: $e');
    }
  }

  /// üÜï Synchronisation des parcours en attente
  Future<void> _onSyncPendingRoutesRequested(
    SyncPendingRoutesRequested event,
    Emitter<RouteGenerationState> emit,
  ) async {
    try {
      emit(state.copyWith(isAnalyzingZone: true));

      await _routesRepository.syncPendingRoutes();
      
      // üÜï D√©clencher la synchronisation dans AppDataBloc
      _appDataBloc?.add(const HistoricDataRefreshRequested());
      
      // Recharger les parcours apr√®s sync
      final routes = await _routesRepository.getUserRoutes();

      emit(state.copyWith(
        isAnalyzingZone: false,
        savedRoutes: routes,
      ));

      print('‚úÖ Synchronisation termin√©e');

    } catch (e) {
      emit(state.copyWith(
        isAnalyzingZone: false,
        errorMessage: 'Erreur de synchronisation: $e',
      ));
    }
  }

